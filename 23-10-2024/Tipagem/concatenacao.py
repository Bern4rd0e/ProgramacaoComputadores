# Em python a adição de duas strings se chama concatenação, isso significa dizer que ele faz a junção de duas ou mais palavras/símbolos.

s1 = "IFRN"
s2 = "CNAT"
s3 = "DIATINF"

lugar = s1 + s2 + s3
print(lugar)
# Saída: IFRNCNATDIATINF

lugar = s1 + '/' + s2 + '/' + s3
print(lugar)
# Saída: IFRN/CNAT/DIATINF