# Método upper()
# Gera uma nova string com
# todos os caracteres maiúsculos

# Método lower()
# • gera um nova string com todos
# os caracteres minúsculos

# Método capitalize()
# • Gera uma nova string com a
# primeira maiúscula e as outras
# minúsculas

# Método swapcase()
# • Gera uma nova string com as
# letras minúsculas convertidas
# para maiúsculas e vice-versa

nome = "Joao Jose Oliveira da Silva"

nome.upper()
# JOAO JOSE OLIVEIRA DA SILVA

nome.lower()
# joao jose oliveira da silva

nome.capitalize()
# Joao Jose Oliveira Da Silva

nome.swapcase()
# jOAO jOSE oLIVEIRA DA sILVA




